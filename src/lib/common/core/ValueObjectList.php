namespace common\core;


/**
 * Class ValueObjectList
 * @package common\core
 */
class ValueObjectList implements \ArrayAccess
{
    /** @var string $class 值对象类 */
    protected $class = ValueObject::class;

    /** @var array $list 值对象数据列表 */
    protected $list = [];

    /**
     * ValueObjectList constructor.
     * @param string $class
     * @param array $list
     */
    public function __construct($class, $list)
    {
        $this->class = $class;
        $this->list = $list;
    }

    /**
     * @return array 列表数据
     */
    public function getList()
    {
        return $this->list;
    }

    /**
     * ValueObjectList newValueObject.
     *
     * @param array $data 值对象数据
     * @return ValueObject 值对象
     */
    protected function newValueObject($data)
    {
        return new $this->class($data);
    }

    /**
     * Whether a offset exists
     * @link http://php.net/manual/en/arrayaccess.offsetexists.php
     * @param mixed $offset <p>
     * An offset to check for.
     * </p>
     * @return boolean true on success or false on failure.
     * </p>
     * <p>
     * The return value will be casted to boolean if non-boolean was returned.
     * @since 5.0.0
     */
    public function offsetExists($offset)
    {
        return isset($this->list[$offset]);
    }

    /**
     * Offset to retrieve
     * @link http://php.net/manual/en/arrayaccess.offsetget.php
     * @param mixed $offset <p>
     * The offset to retrieve.
     * </p>
     * @return ValueObject
     * @since 5.0.0
     */
    public function offsetGet($offset)
    {
        return isset($this->list[$offset]) ? $this->newValueObject($this->list[$offset]) : null;
    }

    /**
     * Offset to set
     * @link http://php.net/manual/en/arrayaccess.offsetset.php
     * @param mixed $offset <p>
     * The offset to assign the value to.
     * </p>
     * @param ValueObject $value <p>
     * The value to set.
     * </p>
     * @return void
     * @since 5.0.0
     */
    public function offsetSet($offset, $value)
    {
        if (is_null($offset)) {
            $this->list[] = $value->getData();
        }
        else {
            $this->list[$offset] = $value->getData();
        }
    }

    /**
     * Offset to unset
     * @link http://php.net/manual/en/arrayaccess.offsetunset.php
     * @param mixed $offset <p>
     * The offset to unset.
     * </p>
     * @return void
     * @since 5.0.0
     */
    public function offsetUnset($offset)
    {
        unset($this->list[$offset]);
    }
}